  <h2><img src="/images/Angel1.gif" width="55" height="121" align="absmiddle" /><span class="Stile2"><u>ANGEL FILM AWARDS - Monaco International Film Festival</span></u> <img src="/images/Angel1d.gif" alt="angel awards" width="55" height="121" align="absmiddle" longdesc="http://www.monacofilmfest.info" /></h2>
  <p style="text-align:center" class="Stile3">PRESS RELEASE - 2014</p>
  <p style="text-align:center" class="Stile3">12<sup>th</sup> ANGEL FILM AWARDS – MONACO INTERNATIONAL FILM FESTIVAL </p>
  
  <p align="justify"><i>The Angel Film Awards director <strong>Rosana Golden</strong> and executive producer <strong>Dean Bentley</strong> are pleased to announce the following....</i></p>
  
  <p style="text-align:center" class="Stile3">4th DECEMBER 2014 - 13:30 OPEN PRESS CALL </p>
  <p style="text-align:center" class="Stile4">THE ART NOVO LOUNGE – NOVOTEL – MONTE CARLO</p>
  
  <p align="justify">The Angel Film Awards VIP press and photo call will take place in the Art Novo Lounge at the <font color="green">Novotel</font>, Monte Carlo. There will be a photo and interview opportunity with the festival filmmakers and cast to include some prolific industry names.</p>
  
  
  <p style="text-align:center" class="Stile3">OFFICIAL JURY PANEL</p>
  
  <p align="justify"><strong>The Official Jury Members - 2014 JURY PRESIDENT</strong> the Eritrean/Italian actress and producer <strong>Zeudi Araya Cristaldi</strong> Italian-American actress <strong>Yvonne Scio</strong>; British author, life coach, journalist <strong>Carole Ann Rice</strong>; actress, diva and TV presenter <strong>Lamitta Frangieh</strong>; Korean writer, director, producer <strong>EUN OH; American/British</strong> executive producer and writer <strong>Rebecca Tranter</strong>.</p>
  
  <p style="text-align:center" class="Stile3">GOODWILL AMBASSADORS</p>
  
  <p align="justify">We are delighted to announce our Goodwill Ambassadors for 2014:  the formidable <strong>Lamia Khashoggi</strong> who is the co-founder of <strong>‘The Children for Peace’</strong> (ONLUS) and is named in <strong>UNESCO’S "A Network of Remarkable Woman"</strong>.  We are also pleased to announce our other Goodwill Ambassador is the remarkable <strong>Debra Mace</strong>, President and co-founder of <strong>“The Children for Peace”</strong>. </p>
  
  <p style="text-align:center" class="Stile3">OFFICAL ANGEL FILM AWARDS PHOTOGRAPHER AND VIDEOGRAPHER</p>
  
  <p align="justify">We would like to announce the AFA Art-house photographer <strong>Anna Neubauer</strong> will be weaving her magic at the festival. The creative film maker & photographer <strong>Mike Colquhoun</strong> will be capturing the best moments with his reportage of photography. The CEO and founder of <strong>‘RIVIERA LIFE TV’ Mark Dezzani</strong> and his team and camera crew <strong>Mungo Sidney Wilmot</strong> will be capturing the essence of this event with a creative reportage filming the event.  Founder of Tendance TV <strong>Stephanie BOYS</strong> will be reporting live from the festival for <strong>Fashion TV</strong>. </p>
  
  <p style="text-align:center" class="Stile3">JOURNALISTIC COVERAGE</p>
  
  <p align="justify">Journalist coverage: TMC - Monte Carlo; Cedric Verany - Nice & Monaco Matin; The Hollywood Reporter; Hello Magazine; Royal Monaco Magazine; Mark Dezzani - Riviera Radio;  Mike Colquhoun - Riviera Buzz; Stephanie Boys  - Tendance TV and Fashion TV; TMC Monaco.Reem Azmy - Al Ahram al Arabi and Renaldo Quacquarini - Movie Scope.  There will be additional UK & French press corps.</p>
  
  <p style="text-align:center" class="Stile3">FESTIVAL LINE UP</p>
  
  <p align="justify">The festival line up consists of <strong>16 premiere films and 10 screenplay nominees</strong> from around the globe, shortlisted for the Angel Film Awards.</p>
  
