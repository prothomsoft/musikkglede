<h2><span class="Stile2">‘CINEMA ANGELS - GIVE A TOY’ 5ème année</span></h2>

<p align="justify">The Lenval Children’s Hospital and Foundation cares for children between the ages of three and twelve. The “Cinema Angels – Give a Toy” appeal was born after the directors of the Angel Film Awards, Rosana and Dean, volunteered to help the children in whatever way they could and now they are reaching out to you, especially with Christmas just around the corner.</p>
<p align="justify" class="Stile18"><strong>Help put a smile on a child’s face… its Christmas time!</strong></p>
<p align="justify"><strong>We ask if you could kindly donate a new soft-toy or teddy bear and deliver on or before the opening press call.</strong></p>


<h2><span class="Stile2">‘THE CHILDREN FOR PEACE’</span></h2>
<p align="justify">In 2005 Lamia co-founded The Children for Peace (ONLUS) an Italian NGO, together with Debra Mace TCFP (ONLUS) President. NGO actively works in Mali with Mujeres Solidarias of the University of Malaga and Concordia Charity along with Princess Marie-Louise of Prussia. Together they fund an orphanage in Ethiopia under the Montserrat Caballè Education Scholarship, giving private educational opportunities to orphaned children. <strong>Information on donating to the cause can be accessed on the festival website – or on the AFA Facebook page.</strong></p> 


<h2><span class="Stile2">Official Festival Opening – Day Screenings</span></h2>

<p align="justify"><strong>There will be Question and Answer sessions with the film directors and cast of all the official films</strong></p>
<p align="justify"><b>15:00 'World Premiere' <font color="#C7234A">"ARLENE"</font> Short Animated Movie (France/UK) (6 min)</b></p>
<p align="justify">Directed & Narrated by Mike Colquhoun, Illustrated by Daria Jabenko, Original Music by Anna Giladi – (Director and crew attending)</p>
<p align="justify">Arlene - Sometime, somewhere, you have a dream, an ambition follow that dream... like Arlene.<br/><br/></p>

<p align="justify"><b>15:10 'European Premiere' <font color="#C7234A">"BINKY"</font> Drama (USA) (9 min)</b></p>
<p align="justify">Written, Directed, Produced and Edited by Stephen David Brooks; DP/Cinematography: David R. Hardberger; Music by Moby</p>
<p align="justify"><font color="#C7234A"><strong>Cast: </strong></font>Billy ‘Sly’ Williams as The Interrogator; Lucy Jenner as Wendy – (Director attending)</p>
<p align="justify">When a troubled woman is questioned by a seemingly detached interrogator they both discover a profound connection that is beyond their wildest dreams.<br/><br/></p>

<p align="justify"><b>15:25 'France/Monaco Premiere' <font color="#C7234A">"DE PIETRA" (MADE OF STONE) </font> Drama (Chile) (10 min)</b></p>
<p align="justify">Directed by Sebastian Olivari; Produced & Original Idea by Fransciso Bassignana; DP/Cinematography: Manuel  Garcia; Composer: Carlos  Sanchez</p>
<p align="justify"><font color="#C7234A"><strong>Cast: </strong></font>The mime: Diego Ruiz; The girl:  Mirella  Granucci - (Producer attending)</p>
<p align="justify">A mime plays the role of a statue every day in the street. One day a mysterious girl starts to smile at him during his performance and this is the beginning of a non-traditional love story...<br/><br/></p>

<p align="justify"><b>15:40 'France/Monaco Premiere' <font color="#C7234A">"LADY LUCK"</font> Family Drama (UK) (18 min)</b></p>
<p align="justify">Directed by Jo Lewis; Written by Jo Lewis and Fay Garrett; DP/Cinematography:  Ewan Mulligan; Editor: Laura Morrod; Composer: Nick Norton - Smith; Costume design: Anisha Fields</p>
<p align="justify"><font color="#C7234A"><strong>Cast: </strong></font>Hester – Abigail Titmuss; Paul – Adam Thomas Wright; Richard – Tav MacDougall; Uncle Oscar – Richard Strange; Basset – <strong>Maurice Byrne</strong>; Bailiff – Mike Simmons - (Director, cast and crew attending)</p>
<p align="justify">DH Lawrence short story ’The Rocking Horse Winner’, about a small boy trying to win back his mother’s love.<br/><br/></p>

<p align="justify"><b>16:00 'European Premiere' <font color="#C7234A">"SUMMER KYOTO"</font> Family Drama (Japan) (88 min)</b></p>
<p align="justify">Written, Directed & Edited by award winning filmmaker Hiroshi TODA; DP/Cinematography Guillaume TAUVERON & Hiroshi TODA; Music: Mica TODA; Producer: Masato HANAZAWA, Mica TODA</p>
<p align="justify"><font color="#C7234A"><strong>Cast: </strong></font>Mr.Nakamura: Yoichi HAYASHI;  Mrs.Nakamura: Hitomi WAKAHARA; The old man: Shyoji YAMADA - (Director, cast and crew attending)</p>
<p align="justify">'Nightfall and a solitary walk, Mr. Nakamura, a scented bag maker, stumbles, upon a helpless old man lying on the street and offers him a place of refuge. After returning the kindness shown to him, the old man disappears...but why?'<br/><br/></p>

