<p align="justify"><b>19:15 'World Premiere' Opera Style Feature Film <font color="#C7234A">"A DREAM OF RED MANSIONS"</font> (A.K.A. Dream of the Red Chamber) Drama (China) (120 min)</b></p>
<p align="justify">Directed by Gong Yingtian; Screenplay by Gong Yingtian & Xu Chunlan (Kunqu Opera Director); DP/Cinematographer: Xie Ping; Screenplay: Wang Yan; Executive Producer: Yang Fengyi; Editor: Jiang Zhaojian; Costume Designer: Peng Dinghuang; Original Music for Voices in Kunqu Opera: Wang Dayuan</p>
<p align="justify"><font color="#C7234A"><strong>Cast: </strong></font>Jia Baoyu: <strong>Weng Jiahui</strong>; Lin Daiyu: <strong>Zhu Bingzhen</strong> (lead cast); Xue Baochai: Shao Tianshuai; Wang Xifeng: Wei Chunrong; Jia Zheng: Wang Yinghui; Madame Jia: Bai Xiaojun (Director, cast & crew attending)</p>
<p align="justify">Based on the mid-eighteenth century novel by Cao Xueqin, "A Dream of Red Mansions" tells the changing fortunes of the Jia Family, focusing on the tragic love of Jia Baoyu and Lin Daiyu. Against such worldly elements as death, financial ruin, marriage, decadence and corruption, Baoyu is finally awakened by a vision, which reveals to him that life is merely a dream, like moonlight mirrored in the water.<br/><br/></p>

